#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new_s;
	size_t	i;

	new_s = (char *)malloc(ft_strlen(s) * sizeof(char) + 1);
	if (new_s == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_s[i] = s[i];
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}
